package com.example.wutai.wutaimoutain.SimiaoSet;

public class Simiao {
    private Integer imageid;
    private String name;
    public Simiao(Integer imageid, String name){
        this.imageid = imageid;
        this.name = name;
    }

    public Integer getImageid() {
        return imageid;
    }

    public String getName() {
        return name;
    }
}
