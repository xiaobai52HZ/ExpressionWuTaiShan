package com.example.wutai.wutaimoutain.yinglian;

public class ShiyiItem {
    private String name,content,sourece;

    public ShiyiItem(String name, String content, String sourece) {
        this.name = name;
        this.content = content;
        this.sourece = sourece;
    }

    public String getName() {
        return name;
    }

    public String getContent() {
        return content;
    }

    public String getSourece() {
        return sourece;
    }
}
